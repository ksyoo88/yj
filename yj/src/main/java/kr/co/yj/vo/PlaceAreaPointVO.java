package kr.co.yj.vo;

public class PlaceAreaPointVO {

	private double mapXMin;
	private double mapXMax;
	private double mapYMin;
	private double mapYMax;
	
	public PlaceAreaPointVO() {}

	public double getMapXMin() {
		return mapXMin;
	}

	public void setMapXMin(double mapXMin) {
		this.mapXMin = mapXMin;
	}

	public double getMapXMax() {
		return mapXMax;
	}

	public void setMapXMax(double mapXMax) {
		this.mapXMax = mapXMax;
	}

	public double getMapYMin() {
		return mapYMin;
	}

	public void setMapYMin(double mapYMin) {
		this.mapYMin = mapYMin;
	}

	public double getMapYMax() {
		return mapYMax;
	}

	public void setMapYMax(double mapYMax) {
		this.mapYMax = mapYMax;
	}
	
}
